function myFunc1() {
    const second  = document.querySelector('.Working-Process__box-bottom');
    const element = document.querySelector('.Working-Process__box');
    const image = document.querySelector(".image-active");
    if(second.style.display == 'none') {
        element.style.backgroundColor = '#B9FF66';
        second.style.display = 'flex';
        image.src="images/Plus-icon-active.png"
        
    } else {
        element.style.backgroundColor = '#F3F3F3';
        second.style.display = 'none';
        image.src="images/Plus-icon-non-active.png"
    }
}

